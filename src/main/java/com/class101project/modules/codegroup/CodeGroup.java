package com.class101project.modules.codegroup;


//1. DTO
public class CodeGroup {

		private String seq;
		private Integer codeGroupCode;
		private String nameKr;
		private String name;
		private Integer codeNum;
		private Integer cgDelNY;
		private Integer cgUseNY;
		private String regDate;
		private String modDate;
		
		
		public String getSeq() {
			return seq;
		}
		public void setSeq(String seq) {
			this.seq = seq;
		}
		public Integer getCodeGroupCode() {
			return codeGroupCode;
		}
		public void setCodeGroupCode(Integer codeGroupCode) {
			this.codeGroupCode = codeGroupCode;
		}
		public String getNameKr() {
			return nameKr;
		}
		public void setNameKr(String nameKr) {
			this.nameKr = nameKr;
		}
		public String getName() {
			return name;
		}
		public void setName(String name) {
			this.name = name;
		}
		public Integer getCodeNum() {
			return codeNum;
		}
		public void setCodeNum(Integer codeNum) {
			this.codeNum = codeNum;
		}
		public Integer getCgDelNY() {
			return cgDelNY;
		}
		public void setCgDelNY(Integer cgDelNY) {
			this.cgDelNY = cgDelNY;
		}
		public Integer getCgUseNY() {
			return cgUseNY;
		}
		public void setCgUseNY(Integer cgUseNY) {
			this.cgUseNY = cgUseNY;
		}
		public String getRegDate() {
			return regDate;
		}
		public void setRegDate(String regDate) {
			this.regDate = regDate;
		}
		public String getModDate() {
			return modDate;
		}
		public void setModDate(String modDate) {
			this.modDate = modDate;
		}
		
		
}
